
public class Factorial {
	public static void main(String[] args) {
		//1. Find the factorial number of 5.
		 int factorial = 1;  
		  int number=5;    
		  for(int i=1;i<=number;i++){    
		      factorial=factorial*i;    
		  }    
		  System.out.println("Factorial number of "+number+" is: "+factorial);    

	}
}
